public class TdcBind {
	private int id;
	private String d_id;
	private String c_id;
	private String d_c_binddate;
	private String d_c_modifydate;
	public TdcBind(){
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getD_id() {
		return d_id;
	}
	public void setD_id(String d_id) {
		this.d_id = d_id;
	}
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getD_c_binddate() {
		return d_c_binddate;
	}
	public void setD_c_binddate(String d_c_binddate) {
		this.d_c_binddate = d_c_binddate;
	}
	public String getD_c_modifydate() {
		return d_c_modifydate;
	}
	public void setD_c_modifydate(String d_c_modifydate) {
		this.d_c_modifydate = d_c_modifydate;
	}
}
